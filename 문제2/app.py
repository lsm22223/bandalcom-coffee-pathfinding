from flask import Flask

# Flask 애플리케이션 인스턴스 생성
app = Flask(__name__)

@app.route("/")
def hello_world():
    """
    루트 경로('/')에 접속했을 때 실행되는 함수
    
    Returns:
        str: "Hello, DevOps!" 문자열
    """
    return "Hello, DevOps!"

# 애플리케이션 실행
if __name__ == "__main__":
    # 호스트: 0.0.0.0 (모든 네트워크 인터페이스에서 접근 가능)
    # 포트: 8080
    # debug=True: 개발 모드 활성화 (코드 변경 시 자동 재시작)
    app.run(host="0.0.0.0", port=8080, debug=True) 